/**
  ******************************************************************************
  * @file    board_io.h
  * @brief   板级IO抽象层 - 硬件操作封装
  * @note    提供统一的硬件访问接口，隔离上层应用与底层驱动
  ******************************************************************************
  */

#ifndef __BOARD_IO_H__
#define __BOARD_IO_H__

#include <stdint.h>
#include <stdbool.h>

/*============================================================================
 *                              板级配置参数
 *                         (根据实际硬件修改以下宏定义)
 *===========================================================================*/
/*----------------------------------------------------------------------------
 *                              ADC配置
 *---------------------------------------------------------------------------*/
/* ADC输出编码数 (12位ADC，NUM_CODES = 2^12 = 4096)
 * 官方公式: VCHANNELx = (VREF+ / NUM_CODES) * ADC_DATAx
 */
#define BOARD_ADC_NUM_CODES         4096.0f

/* MCU供电电压VDDA (V)
 * STM32WLE5典型值3.3V，VREFINT校准也是在此电压下进行
 */
#define BOARD_VDDA_VOLTAGE          3.3f

/* 电池电压分压比
 * 计算公式: 实际电压 = ADC_DATAx / NUM_CODES * VDDA * 分压比
 * 例如: 分压电阻R1=R2=100kΩ，分压比为2.0
 *       测量范围 = 0 ~ (VDDA * 2) = 0 ~ 6.6V
 */
#define BOARD_BAT_DIVIDER           5.4195f

/* 电池电压ADC修正系数
 * 由于ADC输入阻抗偏小，测量值比实际值偏小约0.5%，乘以1.005修正
 */
#define BOARD_BAT_CORRECTION        1.005f

/* ADC采集稳定延时 (ms)，等待分压电路稳定 */
#define BOARD_ADC_SETTLE_DELAY_MS   1

/* ADC转换超时 (ms) */
#define BOARD_ADC_TIMEOUT_MS        10

/*----------------------------------------------------------------------------
 *                              GNSS使能极性
 *---------------------------------------------------------------------------*/
/* GNSS使能引脚有效电平
 * 兼容两种硬件设计:
 *   0 = 高电平有效 (GNSSEN高电平使能GNSS模块)
 *   1 = 低电平有效 (GNSSEN低电平使能GNSS模块)
 * 根据实际硬件修改此宏
 */
#define BOARD_GNSS_EN_ACTIVE_LOW    0

/*----------------------------------------------------------------------------
 *                              内部参考电压
 *---------------------------------------------------------------------------*/
/* STM32WL内部参考电压校准值存储地址
 * 地址: 0x1FFF75AA - 0x1FFF75AB (16位)
 * 该值由ST在生产时校准，校准条件: VDDA = 3.3V, TA = 30°C
 * 
 * 计算实际VDDA: VDDA = 3.3V * VREFINT_CAL / VREFINT_DATA
 * 计算通道电压: VCHANNELx = 3.3V * VREFINT_CAL * ADC_DATAx / (VREFINT_DATA * 4096)
 */
#define BOARD_VREFINT_CAL_ADDR      ((volatile uint16_t *)0x1FFF75AA)

/* 内部参考电压典型值 (V)，仅当校准值不可用时使用 */
#define BOARD_VREFINT_TYPICAL       1.212f

/*----------------------------------------------------------------------------
 *                              系统参数
 *---------------------------------------------------------------------------*/

/*============================================================================
 *                              按键定义
 *===========================================================================*/
typedef enum {
    BTN_UP = 0,     // UP按钮 (PB12)
    BTN_OK,         // OK按钮 (PB2)
    BTN_DOWN,       // DOWN按钮 (PA15)
    BTN_COUNT
} board_btn_t;

/*============================================================================
 *                              电源控制定义
 *===========================================================================*/
typedef enum {
    PWR_ADC_EN = 0,     // 电池电压检测使能 (PB5)
    PWR_5V_EN,          // 5V电源使能 (PB3) - 用于CO传感器供电
    PWR_GNSS_EN,        // GNSS模块使能 (PA8)
    PWR_COUNT
} board_pwr_t;

/*============================================================================
 *                              射频开关模式
 *===========================================================================*/
typedef enum {
    RF_MODE_IDLE = 0,   // 空闲模式 (TX=关, RX=关)
    RF_MODE_TX,         // 发送模式 (TX=开, RX=关)
    RF_MODE_RX,         // 接收模式 (TX=关, RX=开)
} board_rf_mode_t;

/*============================================================================
 *                              LED指示灯
 *===========================================================================*/
typedef enum {
    LED_OFF = 0,        // 关闭
    LED_ON,             // 常亮
    LED_BLINK_SLOW,     // 慢闪 (1Hz)
    LED_BLINK_FAST,     // 快闪 (4Hz)
} board_led_mode_t;

/*============================================================================
 *                              初始化
 *===========================================================================*/
/**
  * @brief  初始化板级IO
  * @note   调用前确保HAL和时钟已初始化
  */
void board_io_init(void);

/*============================================================================
 *                              按键操作
 *===========================================================================*/
/**
  * @brief  读取按键状态
  * @param  btn: 按键ID
  * @retval true: 按下, false: 释放
  */
bool board_btn_read(board_btn_t btn);

/*============================================================================
 *                              电源控制
 *===========================================================================*/
/**
  * @brief  设置电源开关状态
  * @param  pwr: 电源ID
  * @param  on: true-开启, false-关闭
  */
void board_pwr_set(board_pwr_t pwr, bool on);

/**
  * @brief  获取电源开关状态
  * @param  pwr: 电源ID
  * @retval true-开启, false-关闭
  */
bool board_pwr_get(board_pwr_t pwr);

/*============================================================================
 *                              ADC采集
 *===========================================================================*/
/**
  * @brief  读取电池电压
  * @note   会自动使能ADC_EN，读取完成后保持使能状态
  * @retval 电池电压(V)
  */
float board_adc_read_battery(void);

/**
  * @brief  读取内部参考电压
  * @retval 内部参考电压(mV)
  */
uint16_t board_adc_read_vrefint(void);

/**
  * @brief  启动ADC DMA采集
  * @note   启动后可通过board_adc_get_xxx获取最新值
  */
void board_adc_start(void);

/**
  * @brief  停止ADC DMA采集
  */
void board_adc_stop(void);

/**
  * @brief  获取电池电压（DMA模式）
  * @retval 电池电压(V)，需要先调用board_adc_start
  */
float board_adc_get_battery(void);

/**
  * @brief  获取内部参考电压（DMA模式）
  * @retval 内部参考电压(mV)，需要先调用board_adc_start
  */
uint16_t board_adc_get_vrefint(void);

/*============================================================================
 *                              射频开关控制
 *===========================================================================*/
/**
  * @brief  设置射频开关模式
  * @param  mode: 射频模式 (IDLE/TX/RX)
  * @note   TX使能(PA6): 高电平有效
  *         RX使能(PA7): 高电平有效
  */
void board_rf_set_mode(board_rf_mode_t mode);

/**
  * @brief  获取当前射频开关模式
  * @retval 射频模式
  */
board_rf_mode_t board_rf_get_mode(void);

/*============================================================================
 *                              LED指示灯控制
 *===========================================================================*/
/**
  * @brief  设置LED模式
  * @param  mode: LED模式 (OFF/ON/慢闪/快闪)
  */
void board_led_set(board_led_mode_t mode);

/**
  * @brief  LED周期处理 (在主循环中调用)
  * @note   处理闪烁定时
  */
void board_led_process(void);

/*============================================================================
 *                              系统信息
 *===========================================================================*/
/**
  * @brief  获取MCU唯一ID
  * @param  uid: 输出缓冲区(12字节)
  */
void board_get_uid(uint8_t *uid);

/**
  * @brief  获取VREFINT校准值
  * @note   从0x1FFF75AA读取工厂校准值
  * @retval VREFINT校准值(ADC原始值)，0表示读取失败
  */
uint16_t board_get_vrefint_cal(void);

/**
  * @brief  获取MCU温度
  * @retval 温度值(摄氏度，仅作参考)
  */
float board_get_mcu_temp(void);

#endif /* __BOARD_IO_H__ */
